# Hunger System Guide

The hunger system is a core mechanic in Adventurous. Your character needs to eat to survive and maintain their energy levels.

## Hunger Levels

Your hunger status is displayed as a bar in your HUD. There are five levels:

- **Full** (100-76%) - Peak performance, no penalties
- **Satisfied** (75-51%) - Normal, slight energy drain
- **Peckish** (50-26%) - -10% movement speed, -5% combat damage
- **Hungry** (25-1%) - -20% movement speed, -15% combat damage, -10% gathering speed
- **Starving** (0%) - All stats -30%, health decreases over time

## Food System

Different foods restore different amounts of hunger:

| Food       | Hunger Restored | Time to Eat |
| ---------- | --------------- | ----------- |
| Bread      | 25%             | 5 seconds   |
| Meat       | 35%             | 8 seconds   |
| Vegetables | 15%             | 3 seconds   |
| Full Meal  | 50%             | 12 seconds  |

## Managing Hunger

### Prevention

- Eat before going on adventures
- Carry food with you
- Farm crops to have a steady supply

### During Adventures

- You can eat while moving (at reduced speed)
- Hot meals provide temporary buffs (5-10% stats for 10 minutes)
- Some food provides additional effects:
  - **Healing meals** - Restore health while feeding
  - **Energizing meals** - Restore stamina faster

## Regeneration

Hunger decreases based on activity:

- **Resting** - 2% per minute
- **Walking** - 4% per minute
- **Running** - 8% per minute
- **Combat** - 12% per minute
- **Gathering** - 6% per minute

## Economy Impact

Food scarcity can affect:

- Trader prices (food costs more when hunger is common)
- Quest rewards (food-related quests pay more during famines)
- NPC mood (hungry NPCs are less efficient)

## Pro Tips

1. **Cook meals in bulk** when you have time and ingredients
2. **Use energy-efficient travel** to reduce hunger drain
3. **Plan your adventure route** to minimize total hunger loss
4. **Keep backup food** in your inventory at all times
